package com.richest.manager.utils;

import com.richest.manager.domain.User;

public class FinalOpenId {
	private String openId;
	private User user;
	public FinalOpenId(String openid, User u){
		openId = openid;
		user = u;
	}
	public String getOpenId() {
		return openId;
	}
	public User getUser() {
		return user;
	}
}
